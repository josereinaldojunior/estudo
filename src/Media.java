public class Media {
    public static void main(String[] args) {
        int numero1 = 8;
        int numero2 = 9;
        int numero3 = 7;
        int soma1 = numero1 + numero2 + numero3;
        int media1 = soma1 / 3;
        int numero4 = 4;
        int numero5 = 5;
        int numero6 = 6;
        int soma2 = numero4 + numero5 + numero6;
        int media2 = soma2 / 3;
        int somaMedia = media1 + media2;
        int medias = somaMedia / 2;
        System.out.println(" Media 1 : " + media1 );
        System.out.println( " Media 2 : " + media2 );
        System.out.println(" Soma das medias : " + somaMedia );
        System.out.println( " Meida da media : " + medias);
    }
}
